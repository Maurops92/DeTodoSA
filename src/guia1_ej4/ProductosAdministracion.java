    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed

        try {
            int codigo = Integer.parseInt(jtCodigo.getText());
            String descripcion = jtDescripcion.getText();
            double precio = Double.parseDouble(jtPrecio.getText());
            Categoria rubro = (Categoria) jcRubro.getSelectedItem();
            int stock = Integer.parseInt(jtStock.getText());
            boolean verificarCodigo = true;

            for (Producto prod : Menu.listaProductos) {
                if (codigo == prod.getCodigo()) {
                    int confirmResult = JOptionPane.showInternalConfirmDialog(this, "¿Está seguro que desea modificar el producto?", "Confirmación", JOptionPane.YES_NO_OPTION);
                    if (confirmResult == JOptionPane.YES_OPTION) {
                        prod.setDescripcion(descripcion);
                        prod.setPrecio(precio);
                        prod.setRubro(rubro);
                        prod.setStock(stock);
                        JOptionPane.showMessageDialog(this, "El producto " + prod.getDescripcion() + " fue modificado.");
                        verificarCodigo= true;
                        break;
                    }else if (confirmResult == JOptionPane.NO_OPTION){
                       verificarCodigo= true;
                       break;
                    }
                }else{
                    verificarCodigo= false;
                }
            }
            
            if (verificarCodigo== false) {
                JOptionPane.showMessageDialog(this, "No se puede modificar el codigo");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese solo numero en Stock, Precio y Codigo");
        }


    }//GEN-LAST:event_jbGuardarActionPerformed

    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed

        int codigo = Integer.parseInt(jtCodigo.getText());

        Producto producto = null;
        for (Iterator<Producto> it = Menu.listaProductos.iterator(); it.hasNext();) {
            producto = it.next();
            if (producto.getCodigo() == Integer.parseInt(jtCodigo.getText())) {
                int confirmResult = JOptionPane.showInternalConfirmDialog(this, "¿Está seguro que desea eliminar el producto?", "Confirmación", JOptionPane.YES_NO_OPTION);
                if (confirmResult == JOptionPane.YES_OPTION) {
                    Menu.listaProductos.remove(producto);
                    JOptionPane.showMessageDialog(this, "El producto " + producto.getDescripcion() + " fue eliminado.");
                }
                jtDescripcion.setText("");
                jtPrecio.setText("");
                jtStock.setText("");
                jtCodigo.setText("");
                break;
            }
        }

    }//GEN-LAST:event_jbEliminarActionPerformed


