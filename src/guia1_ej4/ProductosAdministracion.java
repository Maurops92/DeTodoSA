    private void jbNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNuevoActionPerformed

        if (jtCodigo.getText().isEmpty() || jtDescripcion.getText().isEmpty() || jtPrecio.getText().isEmpty() || jtStock.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos");
        } else {
            try {
                int codigo = Integer.parseInt(jtCodigo.getText());
                String descripcion = jtDescripcion.getText();
                double precio = Double.parseDouble(jtPrecio.getText());
                Categoria rubro = (Categoria) jcRubro.getSelectedItem();
                int stock = Integer.parseInt(jtStock.getText());
                boolean validacionCodigo = true;

                for (Producto prod : Menu.listaProductos) {
                    if (codigo == prod.getCodigo()) {
                        JOptionPane.showMessageDialog(this, "El codigo ya existe ingrese otro codigo");
                        jtCodigo.setText("");
                        validacionCodigo = true;
                        break;
                    } else {
                        validacionCodigo = false;
                    }
                }

                if (validacionCodigo == false) {
                    Producto pr = new Producto(codigo, descripcion, precio, stock, rubro);
                    Menu.listaProductos.add(pr);
                    JOptionPane.showMessageDialog(this, "Se a guardado correctamente.");
                    jtCodigo.setText("");
                    jtDescripcion.setText("");
                    jtPrecio.setText("");
                    jtStock.setText("");
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Debe ingresar numero en los campos codigo, precio, stock.");
            }

        }
    }//GEN-LAST:event_jbNuevoActionPerformed

