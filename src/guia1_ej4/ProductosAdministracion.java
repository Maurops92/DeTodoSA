    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed

        try {
            int codigo = Integer.parseInt(jtCodigo.getText());
            String descripcion = jtDescripcion.getText();
            boolean validacionCodigo = false;

            for (Producto prod : Menu.listaProductos) {
                if (codigo == prod.getCodigo()) {
                    jtDescripcion.setText(prod.getDescripcion());
                    jtPrecio.setText((String.valueOf(prod.getPrecio())));
                    jtStock.setText(String.valueOf(prod.getStock()));
                    jcRubro.setSelectedItem(prod.getRubro());
                    validacionCodigo = true;
                    break;
                }
            }
            if (validacionCodigo == false) {
                JOptionPane.showMessageDialog(this, "No se encontro un producto con ese codigo");
                jtDescripcion.setText("");
                jtPrecio.setText("");
                jtStock.setText("");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese solo numeros enteros");
        }


    }//GEN-LAST:event_jbBuscarActionPerformed

