    private void jtMayorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtMayorKeyReleased

        borrarFilas();

        try {
            if (jtMenor.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ponga un numero valido para el minimo");
            }
            double min = Double.parseDouble(jtMenor.getText());
            double max = Double.parseDouble(jtMayor.getText());

            if (min > max) {
                JOptionPane.showMessageDialog(this, "El minimo no puede ser mayor que el maximo");
            } else {
                for (Producto prod : Menu.listaProductos) {
                    if (prod.getPrecio() >= min && prod.getPrecio() <= max) {
                        modelo.addRow(new Object[]{
                            prod.getCodigo(),
                            prod.getDescripcion(),
                            prod.getPrecio(),
                            prod.getStock()
                        });
                    }

                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese solo numeros");
            jtMenor.setText("0");
        }


    }//GEN-LAST:event_jtMayorKeyReleased

